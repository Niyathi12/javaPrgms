/*1. Write a program to check if the letter 'e' is present in the word 'Umbrella'.*/

class D3Asgn1{
	public static void main(String[] args){
		String s="Umbrella";
		for(int i=0;i<s.length();i++){
			
			if(s.charAt(i)=='e') {
				System.out.println("'e' is present in umbrella");
			         break; }
		}
}}